    <h1>Asesoría, orientación y trámites de recepción de las solicitudes de créditos</h1>
    <h2>Pasos para orientar a los beneficiarios</h2>
    <p>Los pasos para orientar al beneficiario interesado, serán los siguientes:</p>
    <ol>
      <li>El Director Ejecutivo, asesorará y orientará a la persona interesada en todo lo relacionado con el trámite de una solicitud de crédito.</li>
      <li>La persona interesada entregará en una carpeta desasificada, la fotocopia ampliada de la cédula, copia del recibo de consignación y firma la autorización para consultar, reportar procesar los datos a la CIFIN. La Auxiliar Administrativa y Financiera de la Fundación, recibirá la misma y la radicará en libro de solicitudes de consulta de CIFIN.</li>
      <li>El Director Ejecutivo de la Fundación, realizará la consulta de CIFIN, y en caso de que no tenga inconveniente en el reporte, le entregará al interesado la carpeta con los formatos que debe diligenciar y los demás que debe anexar.</li>
      </ol>
    <h2>Pasos para recepcionar las solicitudes</h2>
    <p>Los pasos para la recepción de las solicitudes de créditos presentadas a la "Fundación los Araucos", serán los siguientes:</p>
    <ol>
      <li>El interesado debe presentar la carpeta de la solicitud de crédito ante el Director Ejecutivo de la Fundación.</li>
      <li>El Director Ejecutivo de la Fundación, revisará con el interesado de la solicitud de crédito, con el fin de confrontar con la lista de chequeo si están todos los documentos para tramitar el crédito.</li>
      <li>El Director Ejecutivo de la Fundación, devolverá al interesado la carpeta de la solicitud de crédito si llegara a faltar uno o varios documentos; en caso, de que la solicitud cumpla con todos los requisitos exigidos será registrada y se le asignará un número en el libro de recepción de solicitudes. </li>
    </ol>

    <h1>Notificación y legalización de las solicitudes de créditos aprobadas por la junta directiva</h1>
    <h2>Proceso de notificación y legalización</h2>
    <p>El Director Ejecutivo una vez aprobada el acta y los acuerdos, iniciará el proceso de notificación y legalización.</p>
    <ol>
      <li>El Director Ejecutivo, una vez firmada el acta por todos los miembros y los acuerdos por presidente y secretario de la Honorable Junta Directiva, procederá a notificar por escrito a cada uno de las personas a las que se le aprobó el crédito o no; con el fin de que los primeros comiencen el proceso de legalización del mismo.</li>
      <li>La persona beneficiada con la aprobación del crédito por parte de la Fundación, deberá legalizar el mismo dentro del mes siguiente a la fecha notificación; con el fin de que constituya las garantías a que haya lugar y firme las mismas.</li>
      <li>El Director Ejecutivo entregará al abogado contratado las garantías constituidas por el beneficiario del crédito; con el fin de que emita su concepto por escrito sobre las mismas.</li>
      </ol>
    <h2>Desembolso y control de inversión</h2>
    <p>El Director Ejecutivo una vez legalizados los créditos, deberá continuar con el proceso de desembolso y control de inversión.</p>
    <ol>
      <li>La Fundación dentro de los cinco (5) días siguientes a la legalización del crédito, realizará el desembolso de los recursos y en el mismo momento entregará copia del pagare al beneficiario y del plan de amortización del crédito.</li>
      <li> El Director posteriormente realizará el control de inversión de los recursos desembolsados para verificar si los recursos fueron invertidos de acuerdo al plan de inversión presentado en el proyecto, en caso afirmativo se procederá a realizar el otro desembolso; en caso contrario se aplicará el reglamento de crédito y cartera.</li>
      </ol>

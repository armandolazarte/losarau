    <h1>Reestructuraciones<h1>
    <h2>Definición de restructuración</h2>
    <p>Se entiende por reestructuración de una obligación, el acuerdo jurídico entre el deudor y la Fundación, que tenga como objeto y efecto mejorar las condiciones de pago del deudor, tales como condonación parcial de interés moratorios y refinanciación; con el fin de recuperar los recursos entregados por la Fundación.<br>
    </p>
    <h2>Definición de refinanciación</h2>
    <p>Se entiende por refinanciación cualquier variación a la estructura crediticia de una operación pactada inicialmente cuyo resultado sea mejorar las condiciones de pago del deudor.<br>
    </p>
    <h2>Requisitos para refinanciación y reestructuración</h2>
    <ol>
      <li>Solicitud escrita diligenciada en el formato preestablecido para ello.<br>
        </li>
      <li>Si la obligación se encuentra en cobro jurídico, presentar paz y salvo expedido por el abogado y pagar a la fundación todos los gastos procesales causados como consecuencia del proceso adelantado contra él. <br>
        </li>
      <li>El deudor deberá abonar como mínimo en efectivo, el cien por ciento de los intereses moratorios y/o corrientes causados.<br>
        </li>
      <li>El nuevo plazo será de acuerdo con el flujo de caja proyectado del deudor, sin exceder el plazo contenido en la línea de crédito respectiva. <br>
        </li>
      <li>Firmar el nuevo pagare junto con su codeudor y los demás documentos que sean necesarios para legalizar el proceso de reestructuración o refinanciación.<br>
        </li>
      <li>Firmar la autorización para la consulta, reporte y procesamiento de datos Financieros en la Central de Información – CIFIN<br>
      </li>
    </ol>
